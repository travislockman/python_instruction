Total = 10
Counter = 0
Loop_Number = 0

while Counter < Total:
    print("This is pass:")
    print(Loop_Number)
    Loop_Number += 1

    print("=============")
    print("Starting loop")
    print(Counter)
    Counter += 1
    print("Ending Loop")
    print("=============")


